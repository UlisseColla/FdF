/* TRANSLATE */
# define UP 0x077
# define DOWN 0x073
# define LEFT 0x061
# define RIGHT 0x064

# define RESET 0x074

/* SCALE */
# define Z_UP 0x075
# define Z_DOWN 0x070

/* ZOOM */
# define ZOOM_IN 0x069
# define ZOOM_OUT 0x06f

/* ROTATE Z */
# define ROTATE_Z_LEFT 0xffbe
# define ROTATE_Z_RIGHT 0xffbf

#endif
